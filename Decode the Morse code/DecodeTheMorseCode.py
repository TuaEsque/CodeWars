def decode_morse(morse_code):
    letters = [l.split(" ") for l in morse_code.split("  ") if l != ""]
    output = []
    for w in letters:
        out_word = [MORSE_CODE[l] for l in w if l in MORSE_CODE and len(l) != 0]
        if len(out_word) != 0:
            output.append(out_word)

    return " ".join(["".join(x) for x in output])